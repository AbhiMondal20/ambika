let timer;
const slidesWrap = document.getElementById('slidesWrap');
const dots = document.querySelectorAll('.dot');
const totalSlides = 4;

function goSlide(n){
  cur = (n + totalSlides) % totalSlides;
  slidesWrap.style.transform = `translateX(-${cur * 100}%)`;
  dots.forEach((d, i) => d.classList.toggle('active', i === cur));
  resetTimer();
}
function nextSlide(){goSlide(cur + 1)}
function prevSlide(){goSlide(cur - 1)}
function resetTimer(){
  clearInterval(timer);
  timer = setInterval(nextSlide, 5500);
}
resetTimer();

let startX = 0;
slidesWrap.addEventListener('touchstart', e => startX = e.touches[0].clientX);
slidesWrap.addEventListener('touchend', e => {
  const diff = startX - e.changedTouches[0].clientX;
  if(Math.abs(diff) > 50) diff > 0 ? nextSlide() : prevSlide();
});

